#!/usr/bin/env node

const fs = require('fs');
const path = require('path');

function parseArgs(argv) {
  const options = {};

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];

    switch (arg) {
      case '--version':
        options.version = requireValue(argv, ++index, arg);
        break;
      case '--repo-root':
        options.repoRoot = requireValue(argv, ++index, arg);
        break;
      case '--help':
      case '-h':
        options.help = true;
        break;
      default:
        throw new Error(`Unknown argument: ${arg}`);
    }
  }

  return options;
}

function requireValue(argv, index, flag) {
  const value = argv[index];
  if (!value || value.startsWith('--')) {
    throw new Error(`${flag} requires a value`);
  }
  return value;
}

function printUsage() {
  console.log(`Usage:
  node tools/release/write-xamarin-version.js --version <semver> [--repo-root <path>]
`);
}

function getAssemblyVersion(version) {
  const match = version.match(/^(\d+)\.(\d+)\.(\d+)/);
  if (!match) {
    throw new Error(`Invalid release version: ${version}`);
  }

  return `${match[1]}.${match[2]}.${match[3]}.0`;
}

function escapeCSharpString(value) {
  return value.replace(/\\/g, '\\\\').replace(/"/g, '\\"');
}

function main() {
  const options = parseArgs(process.argv.slice(2));

  if (options.help) {
    printUsage();
    return;
  }

  if (!options.version) {
    throw new Error('--version is required');
  }

  const repoRoot = path.resolve(options.repoRoot || process.cwd());
  const assemblyVersion = getAssemblyVersion(options.version);
  const informationalVersion = escapeCSharpString(options.version);
  const outputPath = path.join(
    repoRoot,
    'dotnet',
    'M3Mobile.M3Sdk.Xamarin',
    'Properties',
    'GeneratedVersionInfo.cs',
  );

  const content = `// <auto-generated />
using System.Reflection;

[assembly: AssemblyVersion("${assemblyVersion}")]
[assembly: AssemblyFileVersion("${assemblyVersion}")]
[assembly: AssemblyInformationalVersion("${informationalVersion}")]
`;

  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
  fs.writeFileSync(outputPath, content, 'utf8');
  console.log(`Wrote ${path.relative(repoRoot, outputPath)} for ${options.version}`);
}

main();
